package classes;

public class JavaClassesAndObjects { //Create a class named JavaClassesAndObjects
    int x = 5;
    int y =25;

    public static void main(String[] args) {
        JavaClassesAndObjects myObj = new JavaClassesAndObjects(); //Create an object called "myObj" and print the value of x
        JavaClassesAndObjects myObj2 = new JavaClassesAndObjects(); //Multiple Objects

        System.out.println(myObj.x);
        System.out.println(myObj2.y);
    }
}



